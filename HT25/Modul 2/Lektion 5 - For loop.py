# For-loop
# Repetera kod ett visst antal gånger
for _ in range(100):
    print("Kom till skolan i tid!")


# loopvariabel
for i in range(10):
    print(i)

for blablabla in range(23):
    print(blablabla)

# Start, stop
for x in range(13, 37):
    print(x)
# Start, stop, step
for y in range(23, 92, 3):
    print(y)

for z in range(100, 0, -3):
    print(z)



