# Datatyper: str, int, float

# str - string - text
name = "Hampus"
# int - integer - heltal
age = 31

# float - decimaltal
length = 1.80

# print(): skriva ut
print("Hej TEI24A!")

# f-string
print(f"{name} är {age} år gammal och är {length} m lång.")

# input(): skriva in
food = input("What is your favorite food? ")
print(f"{food} is your favorite food.")

# Beräkningar: +, -, *, /

print(2+2)
print(5-3)
print(5*3)
print(8/2)

# ** = upphöjt i
print(5**3)

# ** 0.5 = roten ur
print(9**(0.5))

# // floor division
print(38//3)

# Typkonvertering: str(), int(), float()
